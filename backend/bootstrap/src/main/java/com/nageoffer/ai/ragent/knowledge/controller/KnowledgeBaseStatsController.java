package com.nageoffer.ai.ragent.knowledge.controller;

import com.nageoffer.ai.ragent.framework.convention.Result;
import com.nageoffer.ai.ragent.framework.web.Results;
import com.nageoffer.ai.ragent.knowledge.dto.KnowledgeBaseStats;
import com.nageoffer.ai.ragent.knowledge.service.KnowledgeBaseStatsService;
import lombok.RequiredArgsConstructor;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/api/ragent/knowledge/stats")
@RequiredArgsConstructor
public class KnowledgeBaseStatsController {

    private final KnowledgeBaseStatsService statsService;

    @GetMapping("/{kbId}")
    public Result<KnowledgeBaseStats> getStats(@PathVariable String kbId) {
        return Results.success(statsService.getStats(kbId));
    }
}
